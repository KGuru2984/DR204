package OOPSDemo;

public class PersonalInfo {
	
	// data members
	
	int id;
	String fullname;
	String email;
	long contactno;
	int age;
	
	@Override
	public String toString() {
		return "PersonalInfo [id=" + id + ", fullname=" + fullname + ", email=" + email + ", contactno=" + contactno
				+ ", age=" + age + "]";
	}
	
}
